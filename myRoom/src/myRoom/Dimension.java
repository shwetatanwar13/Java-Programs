package myRoom;

public class Dimension {
   private int length;
   private int width;
   
public Dimension(int length, int width) {
	this.length = length;
	this.width = width;
}
public void setLength(int length) {
	this.length = length;
}
public void setWidth(int width) {
	this.width = width;
}
   
}
